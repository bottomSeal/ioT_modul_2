input_value = int(input())
if input_value == 3:
    print("Треугольник")
elif input_value == 4:
    print("Четырехугольник")
elif input_value == 5:
    print("Пятиугольник")
elif input_value == 6:
    print("Шестиугольник")
elif input_value == 7:
    print("Семиугольник")
elif input_value == 8:
    print("Восьмиугольник")
elif input_value == 9:
    print("Девятиугольник")
elif input_value == 10:
    print("Десятиугольник")
else:
    print("Введенное значние за пределами диапазона")